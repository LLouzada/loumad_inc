/// Defines the routes used in the application.
///
/// The [AppRoutes] class provides constants for the main routes used in the application.
/// These routes are used to navigate between different screens or pages within the application
/// trhough the use of GetX navigation.
class AppRoutes {
  /// The route for the home screen or page.
  static const home = '/';

  /// The route for the about screen or page.
  static const about = '/about';

  /// The route for the settings screen or page.
  static const settings = '/settings';

  /// The route for not found screen or page.
  static const notFound = '/404';
}
